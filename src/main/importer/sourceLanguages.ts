const LANGUAGE_BY_EXTENSION = new Map<string, string>();
for (const [language, extensions] of Object.entries(EXTENSIONS_BY_LANGUAGE)) {
  for (const extension of extensions) {
    const normalized = `.${extension.toLowerCase()}`;
    if (!LANGUAGE_BY_EXTENSION.has(normalized)) LANGUAGE_BY_EXTENSION.set(normalized, language);
  }
}

