const EXCLUDED_SOURCE_FILE_PATTERNS = [
  /(^|\.)min\.(js|mjs|cjs|css)$/i,
  /\.map$/i,
  /(^|\.)(generated|g)\.[^.]+$/i,
  /(^|[-_.])(lock|lockfile)$/i,
  /^(package-lock\.json|npm-shrinkwrap\.json|yarn\.lock|pnpm-lock\.yaml|bun\.lockb?|composer\.lock|cargo\.lock|poetry\.lock|pipfile\.lock|gemfile\.lock|podfile\.lock|pubspec\.lock)$/i
];

export function languageForSemanticSource(relPath: string): string | null {
  const name = path.posix.basename(relPath);
  if (EXCLUDED_SOURCE_FILE_PATTERNS.some((pattern) => pattern.test(name))) return null;
  const byName = LANGUAGE_BY_FILENAME[name.toLowerCase()];
  if (byName) return byName;
  return LANGUAGE_BY_EXTENSION.get(path.posix.extname(name).toLowerCase()) ?? null;
}

